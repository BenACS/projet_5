<h1>Bonjour et bienvenue sur mon site !</h1>
<h2>A quoi sert ce site ?</h2>
<p>Ce site permet de créer et gérer des fiches pour différent jeux de rôles.</p>
<h2>Quel est l'intérêt ?</h2>
<p>L'intérêt de ce site est de pouvoir créer des fiches, mais pas seulement !</br>
	Vous pourrez également imprimer vos fiches.</br>

	Une fois inscris, vos fiches seront enregistrées, vous pourrez par conséquent :
	<ul>
		<li>Afficher une fiche existante plutôt que de la créer à nouveau</li>
		<li>Avoir accès à plusieurs fiches</li>
		<li>Modifier vos fiches quand vous voulez</li>
		<li>Les supprimer</li>
		<li>Les partager</li>
	</ul></br>
	De plus, si vous avez un groupe, vous pourrez utiliser ce site pour jouer avec vos amis depuis chez vous,
	puisque vous pourrez modifier votre personnage à n'importe quel moment.
</p>